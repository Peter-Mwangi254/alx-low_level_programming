#include "3-calc.h"

/**
* op_add - calcultes sum of two integers
* @a: int one
* @b: int two
* Return: returns the sum of a and b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - a function that subtracts two integers
* @a: int one
* @b: int two
* Return: returns the difference of a and b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - a function that multiplies two integers
* @a: int one
* @b: int two
* Return: returns the product of a and b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - a function that divides two integers
* @a: int one
* @b: int two
* Return: returns the result of the division of a by b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - a function that gets the remainder of the division of two integers
* @a: int one
* @b: int two
* Return: returns the remainder of the division of a by b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
